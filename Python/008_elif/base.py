x = input("Please enter a number: ")
sign = input("Please enter an operation: ")
y = input("Please enter another number: ")
if(sign == "+"):
    print(int(x)+int(y))
elif(sign == "-"):
    print(int(x)-int(y))
elif(sign == "*"):
    print(int(x)*int(y))
elif(sign == "/"):
    print(int(x)/int(y))
    


